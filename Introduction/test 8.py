def print_twice(bruce):
    print(bruce)
    print(bruce)


print_twice('spam')
print_twice(42)


def print_triple(wayne):
    print(wayne)
    print(wayne)
    print(wayne)


print_triple("banana")
print_triple("fish")
print_triple(66)


def print_quad(batman):
    print(batman)
    print(batman)
    print(batman)
    print(batman)


print_quad("Bowser")
print_quad("Wario")
print_quad("Waluigi")
print_quad(99)
